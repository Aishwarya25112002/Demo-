/*
 * Q..Write a program to draw following star pattern
 
 * * * *
 * * * *
 * * * *
 */




package Loop;

public class Assig_Que_11 {

	public static void main(String[] args) 
	{
		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=4;j++)
			{
				System.out.print("*");
				
			}
			
			System.out.println();
		}
		
	}

}
